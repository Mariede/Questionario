	ID_PESSOA
	,ID_INFO_SOCIAL_FOLHA_RESPOSTA
	,ID_INFO_SOCIAL_QUESTIONARIO
	,CATEGORIA_ORDEM
	,TOPICO_ORDEM
	,OPCAO_ORDEM;