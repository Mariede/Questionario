-- QUESTIONARIO.VW_QUESTIONARIO_EXIBIR
SELECT
	*
FROM
	QUESTIONARIO.VW_QUESTIONARIO_EXIBIR
ORDER BY
	ID_INFO_SOCIAL_QUESTIONARIO
	,CATEGORIA_ORDEM
	,TOPICO_ORDEM
	,OPCAO_ORDEM;


-- QUESTIONARIO.VW_FAMILIA_EXIBIR
SELECT
	*
FROM
	QUESTIONARIO.VW_FAMILIA_EXIBIR
ORDER BY
	ID_FAMILIA
	,CPF;


